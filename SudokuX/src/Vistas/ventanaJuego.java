        Query q = new Query("consult('test.pl')");
        q.hasSolution();
        q = new Query(coneccion);
        Map<String, Term>[] res = q.allSolutions();
        vacios = res;
       
        Query q2 = new Query("consult('test.pl')");
        q2.hasSolution();
        q2 = new Query(coneccion2);
        Map<String, Term>[] res2 = q2.allSolutions();
